
	function marks() {
	let Tam = 5;
    let Eng = 4;
    let Mat = 7;
    let Sci = 10;
    let Soc = 8;
	

    console.log(" * * * * Ram's Progress Report * * * * ");

	// Equal to Operator ==

    console.log("Tamil mark: ", Tam);
	console.log(Tam == 5);
	console.log("Ram passed in tamil");

	// Not equal to !=

	console.log(Tam != 4);//true
    console.log("Grade is : C");
    console.log("English mark: ", Eng);

    //strict equal operator ===

    console.log(Eng === 4); // true
    console.log("Ram failed in english");

    // strict not equal operator !==

    console.log(Eng !== 4); // false
    console.log("Grade is : F");
    console.log("Maths mark: ",Mat);

    // greater than operator >

    console.log(Mat > 4);//true
    console.log("Ram passed in maths");

    // greater than or equal operator >=

    console.log(Mat >= 4);//true
    console.log("Grade is : B");
    console.log("Science mark: ",Sci);

    // less than operator <

    console.log(Sci < 11);//true
    console.log("Ram passed in science");
    console.log("Grade is : A+");
    console.log("Social mark: ",Soc);

    // less than or equal operator

    console.log(Soc <= 10);//true
    console.log("Ram passed in social");
    console.log("Grade is : B+");
	}
	marks();

